    private void tblUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUserMouseClicked
        int viTri = tblUser.getSelectedRow();
        if(viTri == -1){
            return;   
        }
        Usermanagent us = this.qlds.getList().get(viTri);
        
        txtUserName.setText(us.getUserName());
        txtPass.setText(us.getPassWord());
        if(us.getRole().equalsIgnoreCase("Admin")){
            radioAdmin.setSelected(true);
        
        }else{
            radioUser.setSelected(true);
        }
        
    }//GEN-LAST:event_tblUserMouseClicked

    
    public void ClearForm(){
        this.txtPass.setText("");
        this.txtUserName.setText("");
        this.radioUser.setSelected(true);
    
    }
    
   public void displayTable(){
       DefaultTableModel dtm = (DefaultTableModel) this.tblUser.getModel();
       dtm.setRowCount(0);
      ArrayList<Usermanagent> ds = qlds.getList();
       for (int i = 0; i < ds.size(); i++) {
          Usermanagent us = ds.get(i);             
          Object[] rowData = new Object[]{
              us.getUserName(),
              us.getPassWord(),
              us.getRole(),
          
          };
          dtm.addRow(rowData);
           
       }
       
   
   
   }
    
    

    
    
