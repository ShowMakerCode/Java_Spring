public class Animal {
    private String Ten;
    private String Mau;

    public Animal() {
    }

    public Animal(String Ten, String Mau) {
        this.Ten = Ten;
        this.Mau = Mau;
    }
    public void Tiengkeu(){
    }

    public String getTen() {
        return Ten;
    }

    public void setTen(String Ten) {
        this.Ten = Ten;
    }

    public String getMau() {
        return Mau;
    }

    public void setMau(String Mau) {
        this.Mau = Mau;
    }
    
    
    
    
}
