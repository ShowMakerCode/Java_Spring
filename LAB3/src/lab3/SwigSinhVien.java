    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
         if(txtName.getText().equals("") && txtDiem.getText().equals("")){
            JOptionPane.showMessageDialog(this, " không đc để trống");}
        else if (txtName.getText().equals("") || txtDiem.getText().equals("")){
            JOptionPane.showMessageDialog(this, " không được để trống tên hoặc điểm");   
        }
        else 
        {
        String Nameadd = txtName.getText();
        String Diemadd = txtDiem.getText();
        String Nganhadd = (String) cbbNganh.getSelectedItem();
        String PTADD = "";
        if (cbPT.isSelected() == true) {
            PTADD = "True";
        } else {
            PTADD = "False";
        }
        list.insert(new SINHVIEN(Nameadd, Float.parseFloat(Diemadd), Nganhadd, PTADD));
        JOptionPane.showMessageDialog(this, "Thêm mới thành công");
        DisplayTable();
        }

    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
    if(txtName.getText().equals("") && txtDiem.getText().equals("")){
            JOptionPane.showMessageDialog(this, " không có gì để sửa");}
        else 
        {
        
        
        int viTri = this.tblSinhVien.getSelectedRow();
        if (viTri == -1) {
            return;
        }
        String hoTen = txtName.getText();
        float diem = Float.parseFloat(txtDiem.getText());
        String Nganh = (String) cbbNganh.getSelectedItem();
        String Thuong = "";
        if (cbPT.isSelected() == true) {
            Thuong = "true";
        } else {
            Thuong = "false";
        }
        this.list.Update(viTri, new SINHVIEN(hoTen, diem, Nganh, Thuong));
        DisplayTable();
        JOptionPane.showMessageDialog(this, "sửa thành công");
        ClearForm();
    }//GEN-LAST:event_btnSuaActionPerformed
    }
    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
    if(txtName.getText().equals("") && txtDiem.getText().equals("")){
            JOptionPane.showMessageDialog(this, " không có gì để xóa");}
        else 
        {
        
        int showmess =   JOptionPane.showConfirmDialog(this, " bạn có muốn xóa");
        if(showmess == JOptionPane.YES_OPTION){}
        else if (showmess == JOptionPane.NO_OPTION){
        return;
        }  else if (showmess == JOptionPane.CANCEL_OPTION){
        return;}
        
        int index = this.tblSinhVien.getSelectedRow();
        if (index == -1) {
            return;
        }

        this.list.Delete(index);

        DisplayTable();
        ClearForm();

        }
    }//GEN-LAST:event_btnXoaActionPerformed

