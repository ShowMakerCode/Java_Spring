    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed

        if (txtMaSach.getText().equals("") && txtSoluong.getText().equals("")&&txtTenSach.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Khoong dc de rog");
       
        } else {
        
        
        String maSach = txtMaSach.getText();
        String tenSach = txtTenSach.getText();
        int soLuong = Integer.parseInt(txtSoluong.getText());
        String tinhTrang = "";
        if (cbbTinhTrang.getSelectedIndex() == 0) {
            tinhTrang = "Còn Hàng";
        } else {
             tinhTrang = "Hết Hàng";
        }
        
        
        
        qlds.Insert(new SanPham(maSach, tenSach, soLuong, tinhTrang));
        Displaytable();
        
        }
    }//GEN-LAST:event_btnThemActionPerformed

