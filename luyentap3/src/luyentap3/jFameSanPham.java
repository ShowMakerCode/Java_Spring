    public void Displaytable(){
        DefaultTableModel dtm = (DefaultTableModel) tblQLSP.getModel();
        dtm.setRowCount(0);
        ArrayList<SanPham> sp = qlds.getList();
        for (int i = 0; i < sp.size(); i++) {
        SanPham ds = qlds.getList().get(i);
            Object[] rowData = new Object[]{
                ds.getMaSach(),
                ds.getTenSach(),
                ds.getSoLuong(),
                ds.getTinhTrang(),
           
         };
         dtm.addRow(rowData);
            
        }
    
    }
    
    
      public void Timee() throws InterruptedException {
        Thread t2 = new Thread(new Runnable() {
            @Override
            public void run() {
                while (true) {
                    Date now = new Date();
                    SimpleDateFormat sdf = new SimpleDateFormat();
                    sdf.applyPattern("hh:mm:ss");
                    timingRun.setText(sdf.format(now));
                    try {
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                        ex.printStackTrace();
                    }
                }
            }
        });
        t2.start();
    }

    
