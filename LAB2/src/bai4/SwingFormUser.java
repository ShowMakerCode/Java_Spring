    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(txtUserName.getText().equals("") && txtPass.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Không có dữ liệu để Thêm");
    }else{
        
        
        if(this.txtPass.getText().equals("") && this.txtUserName.getText().equals("")){
           JOptionPane.showMessageDialog(this, " không đc để dữ liệu chống");      
       }   else {
        String Nameadd = this.txtUserName.getText();
        String Passadd = this.txtPass.getText();
        String Quyen = "";
             if (radioUser.isSelected() == true) {
               Quyen = "User";
           } else {
               Quyen = "Admin";
           }
        qlds.Insert(new Usermanagent(Nameadd, Passadd, Quyen));       
        displayTable();     
        ClearForm();
        JOptionPane.showMessageDialog(this, "Thêm Mới Thành Công");
        
        
       }}
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
     
          if(txtUserName.getText().equals("") && txtPass.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Không có dữ liệu để Remove");
    }else{
        
        int xacnhan= JOptionPane.showConfirmDialog(this, "Bạn Muốn Xóa ?");
        if(xacnhan == JOptionPane.YES_OPTION){
        
        }else if(xacnhan == JOptionPane.NO_OPTION){
        return;}else if(xacnhan == JOptionPane.CANCEL_OPTION){
            return;  
        }
        
        int viTri = tblUser.getSelectedRow();
        if(viTri == -1){return;}
        qlds.Delete(viTri);
        displayTable();
        JOptionPane.showMessageDialog(this, "xóa thành công ");
        ClearForm();
          }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
    if(txtUserName.getText().equals("") && txtPass.getText().equals("")){
        JOptionPane.showMessageDialog(this, "Không có dữ liệu để Update");
    }else{
        
      int vitri = tblUser.getSelectedRow();
      if(vitri == -1){
          return;
      }
      String Name = txtUserName.getText();
      String Pass = txtPass.getText();
      String Role = "";
      if(radioAdmin.isSelected() == true){
          Role = "Admin";
      }else{
          Role="User";
        }
       qlds.Update(vitri, new Usermanagent(Name, Pass, Role));
        displayTable();
        JOptionPane.showMessageDialog(this, "Sua Thanh Cong");
        ClearForm();
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

    
