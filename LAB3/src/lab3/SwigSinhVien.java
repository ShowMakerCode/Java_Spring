    private void tblSinhVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSinhVienMouseClicked
        int viTri = this.tblSinhVien.getSelectedRow();
        if (viTri == -1) {
            return;
        }
        SINHVIEN sv = this.list.getList().get(viTri);
        txtName.setText(sv.getHoTen());
        txtHocLuc.setText(sv.getGrade());
        String dieme = String.valueOf(sv.getDiem());
        txtDiem.setText(dieme);
        if (sv.getThuong().equalsIgnoreCase("true")) {
            cbPT.setSelected(true);
        } else {
            cbPT.setSelected(false);
        }

    }//GEN-LAST:event_tblSinhVienMouseClicked

