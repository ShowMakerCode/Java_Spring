    public void ClearForm() {
        txtName.setText("");
        txtDiem.setText("");
        txtHocLuc.setText("");
        cbbNganh.setSelectedIndex(0);
        cbPT.setSelected(false);
    }

    public void DisplayTable() {
        DefaultTableModel dtm = (DefaultTableModel) this.tblSinhVien.getModel();
        dtm.setRowCount(0);
        ArrayList<SINHVIEN> ds = list.getList();
        for (int i = 0; i < ds.size(); i++) {
            SINHVIEN sv = ds.get(i);        
            Object[] rowData = new Object[]{
                sv.getHoTen(),
                sv.getDiem(),
                sv.getNganh(),
                sv.getGrade(),
                sv.getThuong(),};

            dtm.addRow(rowData);

        }

    }

